export const decodedValue = (arr) => {
  return Number(
    COLORS.indexOf(arr[0]).toString() + COLORS.indexOf(arr[1]).toString()
  );
};

const COLORS = [
  'black',
  'brown',
  'red',
  'orange',
  'yellow',
  'green',
  'blue',
  'violet',
  'grey',
  'white',
];
